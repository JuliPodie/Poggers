namespace Poggers.Directions
{
    public enum Direction
    {
        /// <summary>
        /// Key W -> Direction Up
        /// </summary>
        W,

        /// <summary>
        /// Key A -> Direction Left
        /// </summary>
        A,

        /// <summary>
        /// Key S -> Direction Down
        /// </summary>
        S,

        /// <summary>
        /// Key D -> Direction Right
        /// </summary
        D,

        /// <summary>
        /// Key W+D -> Direction Up + Right
        /// </summary>
        WD,

        /// <summary>
        /// Key W+A -> Direction Up + Left
        /// </summary>
        WA,

        /// <summary>
        /// Key S+D -> Direction Down + Right
        /// </summary>
        SD,

        /// <summary>
        /// Key S+A -> Direction Down + Left
        /// </summary>
        SA,
    }
}
